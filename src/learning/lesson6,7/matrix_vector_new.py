"""
Пример работы с матрицами и векторами на новых данных.
- Каждая строка матрицы X представляет студента с характеристиками:
    [баллы за домашку, баллы за тест, активность на уроке]
- Вектор w задает вес каждой характеристики для расчета итоговой оценки
"""

import numpy as np

def main():
    # Матрица студентов (4 студента, 3 характеристики)
    X = np.array([
        [78, 85, 9],
        [92, 76, 7],
        [65, 88, 10],
        [80, 90, 8]
    ])
    print("Матрица X (студенты и их характеристики):\n", X)
    assert X.shape == (4, 3), "Матрица X должна быть размером 4x3"

    # Вектор весов для каждой характеристики
    w = np.array([0.4, 0.4, 0.2])
    print("Вектор w (веса характеристик):", w)
    assert len(w) == 3, "Вектор w должен быть длиной 3"

    # Приведение баллов к диапазону 0-1
    X_scaled = X / np.array([100, 100, 10])
    print("Нормализованная матрица X:\n", X_scaled)

    # Матричное умножение
    final_scores = X_scaled @ w
    print("Итоговые оценки студентов:", final_scores)

    # Вывод результатов построчно
    for i, score in enumerate(final_scores):
        print(f"Студент {i+1}: итоговая оценка = {score:.2f}")

if __name__ == "__main__":
    main()